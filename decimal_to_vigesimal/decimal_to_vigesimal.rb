require 'tbpgr_utils'

results = [['decimal', 'binary', 'oct', 'hex', 'vigesimal']]
[*1..40].each do |decimal|
  memo = [decimal]
  [2, 8, 16, 20].each do |cardinal_number|
    memo << decimal.to_s(cardinal_number)
  end
  results << memo
end

print results.to_table

